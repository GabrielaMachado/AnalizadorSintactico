    private void btnAnalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnalizarActionPerformed
        posInicial = 0;
        posActual = 0;
        cadena = prueba.leer(txtTexto.getText());
        lblTexto.setText(txtTexto.getText());
        txtTexto.setText("");
        if (arrayLexema.isEmpty()) {
            verificarAutomatas(cadena);
        } else {
            arrayLexema.clear();
            verificarAutomatas(cadena);
        }
        jTable1.setModel(llenarTabla());
    }//GEN-LAST:event_btnAnalizarActionPerformed

   public DefaultTableModel llenarTabla() {
        DefaultTableModel modelTabla;
        String nombreColumnas[] = {"Token", "Tipo", "Ubicación"};
        modelTabla = new DefaultTableModel(new Object[][]{}, nombreColumnas);

        try {
            for (int i = 0; i < arrayLexema.size(); i++) {
                System.out.println("Token : " + arrayLexema.get(i).getToken() + " Tipo : " + arrayLexema.get(i).getTipo());

                modelTabla.addRow(new Object[]{
                    arrayLexema.get(i).getToken(),
                    arrayLexema.get(i).getTipo(),
                    arrayLexema.get(i).getCont()
                });
            }
        } catch (Exception e) {
        }
        return modelTabla;
    }    
    
